<script>
    document.addEventListener('DOMContentLoaded', function () {

        // Function to check and toggle download link based on localStorage
        function checkAndToggleDownloadLink(orderId) {
            var downloadButton = document.getElementById('downloadButton');
            var productKey = 'downloadCompleted_' + orderId;
            var downloadCompleted = localStorage.getItem(productKey) === 'true';

            if (downloadCompleted) {
                downloadButton.innerText = 'Download Completed';
                downloadButton.classList.add('disabled');
            }
        }

        // Add event listener to the download button
        var downloadButton = document.getElementById('downloadButton');
        var orderId = document.getElementById('orderId').value;

        checkAndToggleDownloadLink(orderId);

        downloadButton.addEventListener('click', function (event) {
            // Retrieve the filename from data attribute
            var filename = downloadButton.getAttribute('data-filename');
            disableDownloadLink(event, filename, orderId);
        });

        function disableDownloadLink(event, filename, orderId) {
            event.preventDefault();
            var downloadButton = event.target;
            downloadButton.innerText = 'Downloading...';

            // Use jQuery for the AJAX request to mark as downloaded
            $.ajax({
                url: "/mark-as-downloaded/" + encodeURIComponent(orderId),
                type: "POST",
                headers: {
                    'X-CSRF-TOKEN': '{{ csrf_token() }}',
                },
                success: function (data) {
                    if (data.status === 'success') {
                        // Update the button text
                        window.location.href = "/download/" + encodeURIComponent(filename);
                        downloadButton.innerText = 'Download Completed';

                        // Use a unique key to set localStorage for the specific product
                        var productKey = 'downloadCompleted_' + orderId;
                        localStorage.setItem(productKey, 'true');
                        downloadButton.classList.add('disabled');
                    } else {
                        console.error('Error:', data.message);
                    }
                },
                error: function (error) {
                    console.error('Error:', error);
                }
            });
        }
    });
</script>

